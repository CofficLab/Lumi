import Foundation
import LumiPreviewKit

public extension LumiPreviewFacade {

    /// 把 `LumiPreviewKit.PreviewDiscovery` 的 `bodySource` 包装成一段
    /// entry Swift 源，导出 `lumi_preview_make_nsview` C 符号。
    ///
    /// 设计取舍：
    /// - standalone 编译路径仅依赖标准库 / AppKit / SwiftUI / Foundation，不导入用户工程模块。
    /// - planned build 路径由 `PreviewEntryBuilder` 负责导入目标模块；这里保留轻量 entry
    ///   模板用于无工程上下文时的 fallback。
    /// - 符号名与老 `LumiPreviewKit.PreviewEntryBuilder.viewSymbolName` 保持一致
    ///   （`lumi_preview_make_nsview`），后续若接入完整老编译管线产物可零成本兼容。
    /// - 闭包 body 直接拼接进生成源——若 body 引用文件其他类型，需要把同文件源
    ///   一起编译进来（由 `PreviewBuilder` 负责传第二个 .swift 文件给 `swiftc`）。
    enum PreviewEntryGenerator {

        /// 与 `PreviewRenderer.loadDylib(symbolName:)` 默认值保持一致的导出符号名。
        public static let viewSymbolName = "lumi_preview_make_nsview"
        public static let updateViewSymbolName = "lumi_preview_update_nsview"

        /// 默认承载视图尺寸（点）。子进程会在 `resizeSurface` 时再调整 hosting view 的 frame，
        /// 这里给一个非零初值仅是为了让 `NSHostingView.bitmapImageRepForCachingDisplay` 能拿到合法尺寸。
        public static let defaultViewSize = CGSize(width: 320, height: 180)

        /// 生成 entry 源文件的内容。
        ///
        /// - Parameter discovery: `PreviewScanner` 输出的单条预览。
        /// - Returns: 一段可被 `swiftc -emit-library` 编译为 dylib 的 Swift 源。
        public static func generate(for discovery: LumiPreviewFacade.PreviewDiscovery) -> String {
            let body = discovery.bodySource ?? "EmptyView()"
            let rootViewSource = rootViewSource(for: body, layout: discovery.layout)
            return """
            // Auto-generated by LumiPreviewKit.PreviewEntryGenerator.
            // DO NOT EDIT — this file is overwritten on every build.
            //
            // Source preview: \(discovery.title) at \(discovery.sourceFileURL.lastPathComponent):\(discovery.lineNumber)

            import AppKit
            import Foundation
            import SwiftUI

            @_cdecl("\(viewSymbolName)")
            public func \(viewSymbolName)() -> UnsafeMutableRawPointer? {
                let rootView = \(rootViewSource)
                let hosting = NSHostingView(rootView: rootView)
                hosting.frame = NSRect(
                    x: 0,
                    y: 0,
                    width: \(Int(defaultViewSize.width)),
                    height: \(Int(defaultViewSize.height))
                )
                return Unmanaged.passRetained(hosting).toOpaque()
            }

            @_cdecl("\(updateViewSymbolName)")
            public func \(updateViewSymbolName)(_ existingView: UnsafeMutableRawPointer?) -> Bool {
                guard let existingView else { return false }
                let object = Unmanaged<AnyObject>.fromOpaque(existingView).takeUnretainedValue()
                guard let hosting = object as? NSHostingView<AnyView> else { return false }
                hosting.rootView = \(rootViewSource)
                hosting.needsLayout = true
                hosting.needsDisplay = true
                return true
            }
            """
        }

        // MARK: - 私有

        /// 给 `body` 的每一行前面加 `spaces` 个空格，方便嵌入到带缩进的模板里保持可读性。
        private static func indented(_ source: String, spaces: Int) -> String {
            let prefix = String(repeating: " ", count: spaces)
            return source
                .components(separatedBy: "\n")
                .map { line in line.isEmpty ? line : prefix + line }
                .joined(separator: "\n")
        }

        private static func rootViewSource(
            for body: String,
            layout: LumiPreviewFacade.PreviewDiscovery.Layout
        ) -> String {
            let expression: String
            switch layout {
            case .automatic, .sizeThatFits:
                expression = "{\n\(indented(body, spaces: 8))\n}()"
            case let .fixed(width, height):
                expression = "{\n\(indented(body, spaces: 8))\n}()\n.frame(width: \(literal(width)), height: \(literal(height)))"
            }
            return "AnyView(\(expression))"
        }

        private static func literal(_ value: Double) -> String {
            value.rounded() == value ? String(Int(value)) : String(value)
        }
    }
}
